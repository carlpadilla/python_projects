# Dictionary - is an unordered key value pair

dictionary = {
    'a': [1, 2, 3],
    'b': 'hello',
    'x': True
}

my_list = [
    {
        'a': [1, 2, 3],
        'b': 'hello',
        'x': True
    },
    {
        'a': [4, 5, 6],
        'b': 'world',
        'x': False
    }

]

print(dictionary)
print(dictionary['b'])
print(my_list[1]['a'][2])
